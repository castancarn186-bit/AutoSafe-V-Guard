    def generate_dataset(self, output_path='data/raw_commands.csv'):
        """生成数据集"""
        data = []
        
        for standard_intent, info in self.command_templates.items():
            for variant in info['variants']:
                data.append({
                    'command': variant,
                    'category': info['category'],
                    'standard_intent': standard_intent
                })
        
        # 打乱顺序
        random.shuffle(data)
        
        # 创建 DataFrame
        df = pd.DataFrame(data)
        
        # 确保目录存在
        os.makedirs(os.path.dirname(output_path), exist_ok=True)
        
        # 保存
        df.to_csv(output_path, index=False, encoding='utf-8-sig')
        
        print(f"✅ 数据集生成完成！")
        print(f"📁 保存路径：{output_path}")
        print(f"📊 总条数：{len(df)}")
        print(f"📋 指令类别：{df['category'].nunique()}")
        print(f"\n各类别分布:")
        print(df['category'].value_counts())
        
        return df

